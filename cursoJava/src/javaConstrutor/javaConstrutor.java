package javaConstrutor;

public class javaConstrutor {

	String tipo;
	String marca;
	String cor;
	int ano;
	float valor;
	boolean vendido;
	
	
	public javaConstrutor(String tipo, String marca, String cor, int ano, float valor, boolean vendido) {
		
		this.tipo = tipo;
		this.marca = marca;
		this.cor = cor;
		this.ano = ano;
		this.valor = valor;
		this.vendido = vendido;
		
		
		
	}

}
